
"""Conversión de datos"""
"""Conversión de int a string"""

var1 = 2023
var2 = str(var1)

print("El valor de mi varaible var2 es: {}".format(var2))
print("El tipo de dato de mi varaible var2 es: {}".format(type(var2)))


"""Conversión de int a un flotante"""

var3 = 1990
var4 = float(var3)

print("El valor de mi varaible var4 es: {}".format(var4))
print("El tipo de dato de mi varaible var4 es: {}".format(type(var4)))
