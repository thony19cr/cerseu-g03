
"""Operador multiplicador"""

"""Uso del operador: *"""

var1 = 50
multiplicador = 5

resultado = var1*multiplicador
print("El valor de mi variable 'resultado' es: {}".format(resultado))


"""La multiplicación de strings es posible, sólo muestra el string repetido"""
var2 = "Perú\n"
resultado2 = var2 * multiplicador
print("El valor de mi variable 'resultado2' es: {}".format(resultado2))
