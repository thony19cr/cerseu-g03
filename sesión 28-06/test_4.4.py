"""Lista: Eliminar un elemento de la lista indicando un valor"""

varLista = ["Python", "Java", "NodeJS", "Ruby"]

print("El valor de mi lista es: {}".format(varLista))

"""Usando remove para eliminar un elemento"""
varLista.remove("Java")

print("El valor de mi nueva lista es: {}".format(varLista))
