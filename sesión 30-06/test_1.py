
"""Operadores aritméticos en Python"""
"""Potencia"""

base = 4
potencia = 2

resultado = base**potencia

print("El resultado de mi potencia es: {}".format(resultado))

"""Usando la palabra reservada "pow" podemos también elevar a una cierta potencia un número"""
"""Nos pedirá 2 valores, el primero es la base y el segundo es la potencia a ser elevada la base"""

potencia1 = pow(30, 2)
potencia2 = pow(8, 4)

print("El valor de mi potencia1 es: {}".format(potencia1))
print("El valor de mi potencia2 es: {}".format(potencia2))
